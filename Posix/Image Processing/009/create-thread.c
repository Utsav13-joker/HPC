#define width 100 
#define height 72

typedef struct arguments_t {
  int start;
  int stride;
} arguments_t;

unsigned char image[], output[width * height];

int time_variance(struct timespec *start, 
                    struct timespec *end, 
                    long long int *variance) {
  long long int dsec =  end->tv_sec - start->tv_sec; 
  long long int dnsec =  end->tv_nsec - start->tv_nsec; 

  if(dnsec < 0 ) {
    dsec--;
    dnsec += 1000000000; 
  } 
  *variance = dsec * 1000000000 + dnsec;
  return !(*variance > 0);
}


void edge_detect(unsigned char *in, unsigned char *out, arguments_t *args) {
  int x;
  int n_pixels = width * height;

  for(x=0;x<n_pixels;x++) {
    int p, q; 
    int b, g, d, e; 
    int y; 
    
    q = x / width;
    p = x - (width * q);

    if (p == 0 || q == 0 || p == width - 1 || q == height - 1) {
      output[x] = 0;
    } else {
      b = x + width;
      g = x - 1;
      d = x + 1;
      e = x - width;

      y = (in[x] * 4) + (in[b] * -1) + (in[g] * -1) + (in[d] * -1)
          + (in[e] * -1);

      if (y > 0) { 
        out[x] = 255;
      } else {
        out[x] = 0;
      }
    }
  }
}

void *detect_th(void *args){
	edge_detect(image, output, args);
}

void clean_and_exit() {
  exit(0);
}

void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  clean_and_exit();
}

static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, image);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, output);
  glFlush();
}

static void key_pressed(unsigned char key, int p, int q) {
  switch(key){
    case 27: // escape
      clean_and_exit();
      break;
    default: 
      printf("\nPress escape to exit\n");
      break;
  }
}

int main(int argc, char **argv) {

  struct timespec start, end;   
  long long int time_elapsed;
  clock_gettime(CLOCK_MONOTONIC, &start);
  signal(SIGINT, sigint_callback);
 
  printf("image dimensions %dx%d\n", width, height);
  
  pthread_t th1, th2, th3, th4;

  arguments_t th1_arguments;
  th1_arguments.start = 0;
  th1_arguments.stride = 4;

  arguments_t th2_arguments;
  th2_arguments.start = 1;
  th2_arguments.stride = 4;

  arguments_t th3_arguments;
  th3_arguments.start = 2;
  th3_arguments.stride = 4;

  arguments_t th4_arguments;
  th4_arguments.start = 3;
  th4_arguments.stride = 4;

  void edge_detect();
  
  pthread_create(&th1, NULL, detect_th, &th1_arguments);
  pthread_create(&th2, NULL, detect_th, &th2_arguments);
  pthread_create(&th3, NULL, detect_th, &th3_arguments);
  pthread_create(&th4, NULL, detect_th, &th4_arguments);

  pthread_join(th1, NULL);
  pthread_join(th2, NULL);
  pthread_join(th3, NULL);
  pthread_join(th4, NULL);

  clock_gettime(CLOCK_MONOTONIC, &end);
  time_variance(&start, &end, &time_elapsed);
  printf("Time elapsed was %lldnsecs or %0.9lfs\n", time_elapsed,(time_elapsed/1.0e9));


  glutInit(&argc, argv);
  glutInitWindowSize(width * 2,height);
  glutInitDisplayMode(GLUT_SINGLE | GLUT_LUMINANCE);
      
  glutCreateWindow("6CS005 Image Progessing Courework");
  glutDisplayFunc(display);
  glutKeyboardFunc(key_pressed);
  glClearColor(0.0, 1.0, 0.0, 1.0); 

  glutMainLoop(); 

  clean_and_exit();
  
  return 0;
}

